#implicit type casting.

a=10;
b=1.23;
c=a+b;
print("datatype of a :",type(a));
print("datatype of b :",type(b));
print("datatype of c :",type(c));
print("/n");

#Explicit type casting.

a=10;
b=1.23;
c=(float)(a+b);
print("datatype of a :",type(a));
print("datatype of b :",type(b));
print("datatype of c :",type(c));
